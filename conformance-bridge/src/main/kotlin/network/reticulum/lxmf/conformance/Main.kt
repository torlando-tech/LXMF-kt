    "lxmf_decode_bytes" to ::cmdLxmfDecodeBytes,
    "lxmf_shutdown" to ::cmdLxmfShutdown,
)

private fun handleRequest(line: String): JSONObject {
    val request: JSONObject = try {
        JSONObject(line)
    } catch (e: Exception) {
        return JSONObject()
            .put("id", "parse_error")
            .put("success", false)
            .put("error", "JSON parse: ${e.message}")
    }

    val reqId = request.optString("id", "")
    val command = request.optString("command", "")
    val params = request.optJSONObject("params") ?: JSONObject()

    val handler = COMMANDS[command]
        ?: return JSONObject()
            .put("id", reqId)
            .put("success", false)
            .put("error", "Unknown command: $command")

    return try {
        val result = handler(params)
        JSONObject().put("id", reqId).put("success", true).put("result", result)
    } catch (t: Throwable) {
        val sw = java.io.StringWriter()
        t.printStackTrace(java.io.PrintWriter(sw))
        JSONObject()
            .put("id", reqId)
            .put("success", false)
            .put("error", "${t.javaClass.simpleName}: ${t.message}\n$sw")
    }
}

private fun hexToBytes(hex: String): ByteArray {
    val clean = hex.lowercase()
    require(clean.length % 2 == 0) { "Invalid hex length: ${clean.length}" }
    require(clean.all { it in '0'..'9' || it in 'a'..'f' }) {
        "Invalid hex string: contains non-hex characters"
    }
    val out = ByteArray(clean.length / 2)
    for (i in out.indices) {
        out[i] = ((Character.digit(clean[i * 2], 16) shl 4) or
            Character.digit(clean[i * 2 + 1], 16)).toByte()
    }
    return out
}

fun main() {
    // Suppress slf4j-simple noise on stdout — stdout is the JSON-RPC channel.
    // Default level is INFO; bump to WARN. Direct slf4j-simple to stderr.
    System.setProperty("org.slf4j.simpleLogger.defaultLogLevel", "warn")
    System.setProperty("org.slf4j.simpleLogger.logFile", "System.err")

    // Capture original stdout / stderr by reference. Using these
    // references for the JSON-RPC channel and for the redirect target
    // keeps both stable even if a future test framework swaps the
    // System.out / System.err globals out from under us.
    val out = System.out
    val err = System.err
    val reader = BufferedReader(InputStreamReader(System.`in`))

    out.println("READY")
    out.flush()

    // After READY, hijack System.out and route subsequent stdout writes
    // to stderr. The JSON-RPC channel uses the captured `out` directly,
    // so this only redirects unstructured prints. Without this, every
    // `println(...)` in lxmf-kt / reticulum-kt leaks onto the response
    // channel and the test harness silently drops it as non-JSON, so
    // bugs in the underlying stack are completely invisible to anyone
    // running the conformance suite. We point to the captured `err`
    // reference so a future System.err swap doesn't redirect us
    // somewhere unexpected.
    System.setOut(err)

    while (true) {
        val line = try { reader.readLine() } catch (e: Exception) { null }
        if (line == null) break
        val trimmed = line.trim()
        if (trimmed.isEmpty()) continue
        val response = handleRequest(trimmed)
        out.println(response.toString())
        out.flush()
    }
    exitProcess(0)
}
