    cmdLxmfSendCommon(params, DeliveryMethod.PROPAGATED, "lxmf_send_propagated")

// Terminal states: COMPLETE, FAILED, NO_PATH, NO_LINK.
//
// IDLE is intentionally NOT in this set. IDLE is also the router's
// default resting state BEFORE a transfer begins, so if we read the
// state before requestMessagesFromPropagationNode() has had a chance
// to advance it past IDLE, we'd exit immediately and report
// "final_state=idle" without the transfer having run at all. We track
// "saw busy" separately and only treat IDLE as terminal once a busy
// state was observed.
private val TERMINAL_PROPAGATION_STATES = setOf(
    LXMRouter.PropagationTransferState.COMPLETE,
    LXMRouter.PropagationTransferState.FAILED,
    LXMRouter.PropagationTransferState.NO_PATH,
    LXMRouter.PropagationTransferState.NO_LINK,
)

private val BUSY_PROPAGATION_STATES = setOf(
    LXMRouter.PropagationTransferState.PATH_REQUESTED,
    LXMRouter.PropagationTransferState.LINK_ESTABLISHING,
    LXMRouter.PropagationTransferState.LINK_ESTABLISHED,
    LXMRouter.PropagationTransferState.LISTING_MESSAGES,
    LXMRouter.PropagationTransferState.REQUESTING_MESSAGES,
    LXMRouter.PropagationTransferState.RECEIVING_MESSAGES,
)

/**
 * Block until propagation sync is quiescent or the timeout expires.
 *
 * NOTE on the dispatch loop: this command is intentionally synchronous,
 * which means while we sleep/poll here the bridge's stdin dispatch loop
 * (`fun main()` while-loop) cannot service further commands until we
 * return. That's the contract — the conformance harness sends
 * `lxmf_sync_inbound` and waits for the result before proceeding, and
 * tests are sequenced one-command-at-a-time per bridge subprocess. If
 * the contract ever changes to allow concurrent commands while a sync
 * is in flight, this would need to dispatch the wait to a worker thread
 * and report completion via a separate poll command. For today's usage
 * the blocking shape is the right model.
 */
private fun cmdLxmfSyncInbound(params: JSONObject): JSONObject {
    val router = ensureRouter("lxmf_sync_inbound")
    val timeoutSec = params.optDouble("timeout_sec", 30.0)

    router.requestMessagesFromPropagationNode()

    val deadlineMs = System.currentTimeMillis() + (timeoutSec * 1000).toLong()
    var finalState = router.propagationTransferState
    var sawBusy = false
    while (System.currentTimeMillis() < deadlineMs) {
        finalState = router.propagationTransferState
        if (finalState in BUSY_PROPAGATION_STATES) {
            sawBusy = true
        }
        // Hard terminal: COMPLETE / FAILED / NO_PATH / NO_LINK — exit immediately.
        // Soft terminal: IDLE only counts after a busy state was seen, so we
        // don't bail on the pre-transfer resting state before the request has
        // had a chance to schedule any work.
        val terminalNow = finalState in TERMINAL_PROPAGATION_STATES ||
            (sawBusy && finalState == LXMRouter.PropagationTransferState.IDLE)
        if (terminalNow) {
            // Capture the terminal state BEFORE the settle. A COMPLETE
            // state normally cycles to IDLE during the brief settle
            // window; if we then re-read, we'd return "idle" and a
            // fixture that asserts "complete" would treat the sync as
            // never having started. Mirrors the python bridge's 0.5s
            // tail for in-flight observability without using the
            // post-settle value for the return.
            val terminalState = finalState
            Thread.sleep(500)
            return JSONObject().put("final_state", terminalState.name.lowercase())
        }
        Thread.sleep(200)
    }

    return JSONObject().put("final_state", finalState.name.lowercase())
}

private fun cmdLxmfGetReceivedMessages(params: JSONObject): JSONObject {
    ensureRouter("lxmf_get_received_messages")
    val sinceSeq = params.optInt("since_seq", 0)
    val out = JSONArray()
    val lastSeq: Int
    BridgeState.inboxLock.withLock {
        for (m in BridgeState.inbox) {
            if (m.getInt("seq") > sinceSeq) out.put(m)
        }
        lastSeq = BridgeState.inboxSeq
    }
    return JSONObject().put("messages", out).put("last_seq", lastSeq)
}

private fun cmdLxmfGetMessageState(params: JSONObject): JSONObject {
    ensureRouter("lxmf_get_message_state")
    val hashHex = params.getString("message_hash")
    val recordedState = BridgeState.outboundState[hashHex]

    // Prefer the recorded state when it's already terminal — those values
    // come from the actual deliveryCallback / failedCallback firings and
    // are authoritative. For non-terminal recorded states (or no record
    // at all), fall back to the live LXMessage's current state. The
    // router progresses messages through OUTBOUND → SENDING → SENT →
    // DELIVERED but only invokes deliveryCallback at terminal-for-method
    // (DELIVERED for opportunistic, SENT for propagated). The
    // intermediate transitions never reach the recorded map, so polling
    // it alone misses progress that already happened on the message.
    val state = if (recordedState in TERMINAL_OUTBOUND_STATES) {
        recordedState!!
    } else {
        val live = BridgeState.outboundMessages[hashHex]?.state
        if (live != null) stateToString(live) else (recordedState ?: "unknown")
    }
    return JSONObject().put("state", state)
}

/**
 * Decode raw LXMF wire bytes and return parsed components + computed hash.
 *
 * Byte-level conformance command. Does NOT require lxmf_init — operates
 * on the LXMessage decoder in isolation. The whole point is to exercise
 * the msgpack/payload parsing layer directly, against payload shapes
 * that may be impossible to elicit through normal send commands (e.g.
 * a fields slot encoded as msgpack Nil, which kotlin's own sender never
 * emits but iOS LXMF does — and which kotlin's decoder used to throw on,
 * silently dropping every iOS-originated message).
 *
 * Mirrors lxmf_python.py::cmd_lxmf_decode_bytes.
 *
 * Implements the same logic shape as LXMessage.unpackFromBytes (the very
 * code path under test) but without going through Identity recall /
 * signature validation, so test bytes can use a dummy signature and
 * unknown destination/source hashes.
 */
private fun cmdLxmfDecodeBytes(params: JSONObject): JSONObject {
    val DEST_LEN = 16
    val SIG_LEN = 64

    val lxmfBytesHex = params.optString("lxmf_bytes", "")
    val lxmfBytes = try {
        hexToBytes(lxmfBytesHex)
    } catch (e: Exception) {
        return JSONObject().put("decode_error", "hex parse: ${e::class.simpleName}: ${e.message}")
    }

    if (lxmfBytes.size < 2 * DEST_LEN + SIG_LEN) {
        return JSONObject().put("decode_error", "lxmf_bytes too short: ${lxmfBytes.size}")
    }

    // Call the production decoder directly. This is the WHOLE POINT of the
    // command — to exercise the actual LXMessage.unpackFromBytes code path
    // that runs in the wild, not a parallel re-implementation. A bridge
    // that re-implemented decode here would silently mask production
    // decoder bugs (e.g. the iOS-Nil-fields landmine that took weeks to
    // find precisely because the conformance suite couldn't reach the
    // production code path).
    val msg = network.reticulum.lxmf.LXMessage.unpackFromBytes(lxmfBytes)
        ?: return JSONObject().put(
            "decode_error",
            "LXMessage.unpackFromBytes returned null — see bridge stderr for the underlying exception"
        )

    // Detect whether the wire form had Nil fields by re-parsing just that
    // byte position. This is shape-level — production keeps fields as a
    // (possibly empty) Map; the wire-side Nil/Map distinction is only
    // load-bearing through the hash computation which production already
    // handles. For the test surface, expose it diagnostically.
    val packedPayload = lxmfBytes.copyOfRange(2 * DEST_LEN + SIG_LEN, lxmfBytes.size)
    val fieldsWasNil = try {
        val u = org.msgpack.core.MessagePack.newDefaultUnpacker(packedPayload)
        u.unpackArrayHeader()
        u.skipValue() // timestamp
        u.skipValue() // title
        u.skipValue() // content
        val isNil = u.nextFormat.valueType.name == "NIL"
        u.close()
        isNil
    } catch (_: Exception) {
        false
    }

    val title = msg.title
    val content = msg.content
    val titleHex = title.toByteArray(Charsets.UTF_8).toHexString()
    val contentHex = content.toByteArray(Charsets.UTF_8).toHexString()

    return JSONObject()
        .put("destination_hash", msg.destinationHash.toHexString())
        .put("source_hash", msg.sourceHash.toHexString())
        .put("signature", (msg.signature ?: ByteArray(0)).toHexString())
        .put("timestamp", msg.timestamp ?: 0.0)
        .put("title_hex", titleHex)
        .put("content_hex", contentHex)
        .put("fields_was_nil", fieldsWasNil)
        .put("fields_count", msg.fields.size)
        .put("stamp", msg.stamp?.toHexString() ?: JSONObject.NULL)
        .put("message_hash", (msg.hash ?: ByteArray(0)).toHexString())
}

private fun cmdLxmfShutdown(params: JSONObject): JSONObject {
    // Set the shuttingDown flag FIRST so any callbacks the router fires
    // during teardown can't repopulate cleared collections behind us.
    // The flag is checked by recordOutboundState() and the
    // registerDeliveryCallback closure.
    BridgeState.shuttingDown = true
    var stopped = false
    val router = BridgeState.router
    if (router != null) {
        try { router.stop() } catch (_: Throwable) {}
        BridgeState.router = null
        stopped = true
    }
    for (iface in BridgeState.interfaces) {
        try { iface.detach() } catch (_: Throwable) {}
    }
    BridgeState.interfaces.clear()
    try { Reticulum.stop() } catch (_: Throwable) {}
    BridgeState.reticulum = null
    BridgeState.identity = null
    BridgeState.deliveryDestination = null

    // Best-effort cleanup of tempdirs.
    // configDir is always auto-generated by lxmf_init, so always delete.
    // storagePath is only deleted when the bridge created it; a caller-
    // supplied path is left in place so a fixture that reuses storage
    // across multiple trios doesn't get its data wiped.
    if (BridgeState.storagePathAutoCreated) {
        BridgeState.storagePath?.let { runCatching { File(it).deleteRecursively() } }
    }
    BridgeState.configDir?.let { runCatching { File(it).deleteRecursively() } }
    BridgeState.storagePath = null
    BridgeState.storagePathAutoCreated = false
    BridgeState.configDir = null

    BridgeState.inboxLock.withLock {
        BridgeState.inbox.clear()
        BridgeState.inboxSeq = 0
    }
    BridgeState.outboundState.clear()
    BridgeState.outboundMessages.clear()

    return JSONObject().put("stopped", stopped)
}

