private fun cmdLxmfInit(params: JSONObject): JSONObject {
    if (BridgeState.router != null) {
        throw IllegalStateException(
            "lxmf_init has already been called on this bridge process. " +
                "Spawn a separate bridge subprocess for each LXMF node."
        )
    }

    // Reset shuttingDown — `cmdLxmfShutdown` sets it true to gate late
    // callbacks from repopulating cleared collections, but it stays true
    // forever otherwise. If a caller invokes `lxmf_init` again after
    // `lxmf_shutdown` (even though the contract is one-shot per process,
    // we don't want a stale flag to silently no-op every callback that
    // checks it). The router-null guard above protects against the
    // intended-one-shot violation; this just makes the state clean if
    // it does happen.
    BridgeState.shuttingDown = false

    val callerStoragePath = params.optString("storage_path", "")
    val storagePathAutoCreated = callerStoragePath.isEmpty()
    val storagePath = if (storagePathAutoCreated) {
        Files.createTempDirectory("lxmf_conf_storage_").toAbsolutePath().toString()
    } else {
        callerStoragePath
    }
    val configDir = Files.createTempDirectory("lxmf_conf_rns_").toAbsolutePath().toString()
    val displayName = params.optString("display_name", "lxmf-conformance-peer-kt")

    // Write a minimal Reticulum config so RNS doesn't auto-generate a
    // share_instance / LocalServerInterface that would collide between
    // bridges in the same test.
    val configFile = File(configDir, "config")
    configFile.writeText(
        """
        |[reticulum]
        |  enable_transport = No
        |  share_instance = No
        |  respond_to_probes = No
        |
        |[interfaces]
        |
        """.trimMargin()
    )

    // Reticulum.start in reticulum-kt uses configDir to load identity etc.
    val rns = Reticulum.start(
        configDir = configDir,
        enableTransport = false,
        shareInstance = false,
    )

    // From here on, anything that throws must call Reticulum.stop() so we
    // don't leak the running RNS instance across bridge restarts. Lxmf_init
    // is idempotent at the contract level (one bridge process = one router)
    // but a partial init that left RNS running would prevent subsequent
    // tests in the same harness from re-establishing.
    val router: LXMRouter
    val deliveryDest: Destination
    val identity: Identity
    try {
        // Fresh identity per bridge process. Matches python reference.
        identity = Identity.create()
        router = LXMRouter(identity = identity, storagePath = storagePath)

        deliveryDest = router.registerDeliveryIdentity(identity, displayName = displayName)

        router.registerDeliveryCallback { message ->
            // Drop late callbacks that fire on background threads after
            // cmdLxmfShutdown cleared the inbox. Otherwise they re-add
            // the entry and the next test sees stale state.
            if (BridgeState.shuttingDown) return@registerDeliveryCallback
            val entry = JSONObject()
            entry.put("message_hash", message.hash?.toHexString() ?: "")
            entry.put("source_hash", message.sourceHash.toHexString())
            entry.put("destination_hash", message.destinationHash.toHexString())
            entry.put("title", message.title)
            entry.put("content", message.content)
            entry.put("method", methodToString(message.method))
            entry.put("ack_status", "received")
            entry.put("received_at_ms", System.currentTimeMillis())
            entry.put("fields", encodeMessageFields(message))
            // Increment seq AND add to inbox under a single lock acquisition.
            // Splitting them across two `withLock` blocks lets a poll between
            // the two release `last_seq=N` (already incremented) without the
            // entry seq=N in inbox — the next poll advances `since_seq` past
            // it and the entry is silently dropped.
            BridgeState.inboxLock.withLock {
                BridgeState.inboxSeq += 1
                entry.put("seq", BridgeState.inboxSeq)
                BridgeState.inbox.add(entry)
            }
        }

        router.start()
    } catch (t: Throwable) {
        runCatching { Reticulum.stop() }
        // configDir is always auto-created above; storagePath only when
        // the caller didn't supply one. Skip caller-supplied paths so a
        // retry path can't nuke their fixtures. Best-effort — log nothing
        // because we're about to rethrow and the caller's stack trace
        // is what matters.
        runCatching { File(configDir).deleteRecursively() }
        if (storagePathAutoCreated) {
            runCatching { File(storagePath).deleteRecursively() }
        }
        throw t
    }

    BridgeState.reticulum = rns
    BridgeState.router = router
    BridgeState.identity = identity
    BridgeState.deliveryDestination = deliveryDest
    BridgeState.configDir = configDir
    BridgeState.storagePath = storagePath
    BridgeState.storagePathAutoCreated = storagePathAutoCreated

    val result = JSONObject()
    result.put("identity_hash", identity.hash.toHexString())
    result.put("delivery_destination_hash", deliveryDest.hash.toHexString())
    result.put("config_dir", configDir)
    result.put("storage_path", storagePath)
    return result
}

private fun cmdLxmfAddTcpServerInterface(params: JSONObject): JSONObject {
    ensureRouter("lxmf_add_tcp_server_interface")
    val bindPort = params.optInt("bind_port", 0).takeIf { it > 0 } ?: allocateFreePort()
    val name = params.optString("name", "tcpserver").ifEmpty { "tcpserver" }

    val iface = TCPServerInterface(
        name = name,
        bindAddress = "127.0.0.1",
        bindPort = bindPort,
    )

    // Register each spawned child interface with Transport BEFORE start()
    // opens the accept loop. The parent TCPServerInterface's processOutgoing
    // is intentionally a no-op (post-#46) so that Transport routes outbound
    // packets via the spawned child that owns the actual socket. If the
    // spawned child isn't registered with Transport, findInterfaceByHash
    // returns null when an outbound link packet looks up its receiving
    // interface — and the packet is silently dropped. This manifests as
    // any DIRECT-from-this-bridge LXMF send appearing to "succeed" (link
    // handshake completes) while the receiver never sees the data packet.
    // Mirrors the equivalent wiring in Columba's NativeInterfaceFactory.kt
    // and reticulum-kt PR #47.
    iface.onClientConnected = { spawned ->
        runCatching {
            Transport.registerInterface(spawned.toRef())
        }.onFailure { e ->
            System.err.println(
                "[bridge] Failed to register spawned client ${spawned.name}: ${e.message}"
            )
        }
    }
    // Symmetric deregister so a long-running bridge with repeated
    // connect/disconnect cycles doesn't accumulate stale closed-socket
    // entries in Transport.interfaces. The upstream
    // TCPServerInterface.clientDisconnected (rns-interfaces v0.0.15+)
    // already invokes Transport.deregisterInterface internally on the
    // child it owns; this hook is a belt-and-suspenders second call to
    // the same API, idempotent if the child was already removed.
    iface.onClientDisconnected = { spawned ->
        runCatching {
            Transport.deregisterInterface(spawned.toRef())
        }
    }

    iface.start()
    Transport.registerInterface(iface.toRef())
    BridgeState.interfaces.add(iface)

    return JSONObject().put("port", bindPort).put("interface_name", name)
}

private fun cmdLxmfAddTcpClientInterface(params: JSONObject): JSONObject {
    ensureRouter("lxmf_add_tcp_client_interface")
    val targetHost = params.optString("target_host", "127.0.0.1").ifEmpty { "127.0.0.1" }
    val targetPort = params.getInt("target_port")
    val name = params.optString("name", "tcpclient").ifEmpty { "tcpclient" }

    val iface = TCPClientInterface(
        name = name,
        targetHost = targetHost,
        targetPort = targetPort,
    )
    iface.start()
    Transport.registerInterface(iface.toRef())
    BridgeState.interfaces.add(iface)

    return JSONObject().put("interface_name", name)
}

private fun cmdLxmfAnnounce(params: JSONObject): JSONObject {
    val router = ensureRouter("lxmf_announce")
    val dest = BridgeState.deliveryDestination
        ?: throw IllegalStateException("Delivery destination not registered")
    router.announce(dest)
    return JSONObject().put("delivery_destination_hash", dest.hash.toHexString())
}

private fun makeOutboundDelivery(destHashHex: String): Destination {
    val destHash = hexToBytes(destHashHex)
    val recipientIdentity = Identity.recall(destHash)
        ?: throw IllegalStateException(
            "No identity known for destination $destHashHex. The recipient " +
                "must announce its delivery destination before this peer can send to it."
        )
    return Destination.create(
        identity = recipientIdentity,
        direction = DestinationDirection.OUT,
        type = DestinationType.SINGLE,
        appName = "lxmf",
        "delivery",
    )
}

/**
 * Shared body for the three lxmf_send_* JSON-RPC commands. They differ
 * only in the DeliveryMethod enum value and the routerErrorTag used in
 * the not-yet-initialised error message. Keeping the shape in one place
 * means callback wiring, hash recording, and the
 * runBlocking-handleOutbound boundary can't drift between methods.
 */
private fun cmdLxmfSendCommon(
    params: JSONObject,
    method: DeliveryMethod,
    routerErrorTag: String,
): JSONObject {
    val router = ensureRouter(routerErrorTag)
    val source = BridgeState.deliveryDestination
        ?: throw IllegalStateException("source delivery destination not registered")

    val destHashHex = params.getString("destination_hash")
    val content = params.getString("content")
    val title = params.optString("title", "")

    val recipientDestination = makeOutboundDelivery(destHashHex)

    val message = LXMessage.create(
        destination = recipientDestination,
        source = source,
        content = content,
        title = title,
        fields = decodeFieldsParam(params),
        desiredMethod = method,
    )
    // Per-message callbacks so we can track state transitions.
    message.deliveryCallback = { m ->
        m.hash?.let { recordOutboundState(it.toHexString(), stateToString(m.state)) }
    }
    message.failedCallback = { m ->
        m.hash?.let { recordOutboundState(it.toHexString(), stateToString(m.state)) }
    }

    runBlocking { router.handleOutbound(message) }
    val msgHashHex = message.hash?.toHexString() ?: ""
    if (msgHashHex.isNotEmpty()) {
        recordOutboundState(msgHashHex, stateToString(message.state))
        // Keep a live reference so cmdLxmfGetMessageState can read the
        // current message.state directly. The router advances state
        // through transitions (OUTBOUND → SENT for opportunistic;
        // OUTBOUND → SENDING → SENT for propagated) without firing the
        // deliveryCallback for the intermediate ones, so polling
        // outboundState alone misses real progress.
        BridgeState.outboundMessages[msgHashHex] = message
    }

    return JSONObject().put("message_hash", msgHashHex)
}

private fun cmdLxmfSendOpportunistic(params: JSONObject): JSONObject =
    cmdLxmfSendCommon(params, DeliveryMethod.OPPORTUNISTIC, "lxmf_send_opportunistic")

private fun cmdLxmfSendDirect(params: JSONObject): JSONObject =
    cmdLxmfSendCommon(params, DeliveryMethod.DIRECT, "lxmf_send_direct")

// ----------------------------------------------------------------------
// Path probing
// ----------------------------------------------------------------------
//
// has_path requires BOTH Transport.hasPath() AND Identity.recall(). The
// LXMF send path needs the identity to encrypt the LXM, so a fixture
// that polls only on hasPath() can race past identity-recall and the
// next send fails. Mirrors the python bridge which made the same mistake
// once and got bitten — keep the AND-of-both contract identical.

private fun cmdLxmfHasPath(params: JSONObject): JSONObject {
    ensureRouter("lxmf_has_path")
    val destHash = hexToBytes(params.getString("destination_hash"))
    val transportHasPath = Transport.hasPath(destHash)
    val identityRecalled = Identity.recall(destHash) != null
    return JSONObject()
        .put("has_path", transportHasPath && identityRecalled)
        .put("transport_has_path", transportHasPath)
        .put("identity_recalled", identityRecalled)
}

private fun cmdLxmfRequestPath(params: JSONObject): JSONObject {
    ensureRouter("lxmf_request_path")
    val destHash = hexToBytes(params.getString("destination_hash"))
    Transport.requestPath(destHash)
    return JSONObject().put("requested", true)
}

