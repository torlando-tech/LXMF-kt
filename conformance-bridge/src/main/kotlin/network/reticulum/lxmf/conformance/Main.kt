/**
 * LXMF Kotlin conformance bridge.
 *
 * JSON-RPC over stdio, one object per line. Mirrors the python reference
 * at lxmf-conformance/reference/lxmf_python.py command-for-command. See
 * the README in the lxmf-conformance repo for the contract.
 *
 * Wire protocol:
 *   - On startup, prints exactly "READY" on stdout, then enters loop.
 *   - Reads JSON requests from stdin, one per line:
 *       {"id": "req-N", "command": "...", "params": {...}}
 *   - Writes JSON responses on stdout, one per line:
 *       {"id": "req-N", "success": true, "result": {...}}
 *       {"id": "req-N", "success": false, "error": "..."}
 *
 * stderr is left for log noise; stdout is sacred — the test harness
 * filters non-JSON lines on stdout but it's still fragile, so we
 * suppress slf4j-simple at WARN.
 */

// ----------------------------------------------------------------------
// Bridge state. Single-instance per process, mirroring lxmf_python.py.
// ----------------------------------------------------------------------

private object BridgeState {
    @Volatile var reticulum: Reticulum? = null
    @Volatile var router: LXMRouter? = null
    @Volatile var identity: Identity? = null
    @Volatile var deliveryDestination: Destination? = null
    @Volatile var configDir: String? = null
    @Volatile var storagePath: String? = null

    // True only when storagePath was auto-generated by lxmf_init because
    // the caller didn't supply one. lxmf_shutdown's recursive-delete must
    // skip caller-supplied paths so a fixture that reuses storage across
    // multiple trios doesn't get its data wiped.
    @Volatile var storagePathAutoCreated: Boolean = false

    // Inbound message queue. Append-only with monotonic seq.
    val inbox = mutableListOf<JSONObject>()
    val inboxLock = ReentrantLock()
    @Volatile var inboxSeq = 0

    // Outbound message state map: hex -> state name. Updated by the
    // per-message deliveryCallback / failedCallback closures plus a
    // synchronous post-handleOutbound write in each cmdLxmf_send_*.
    val outboundState = ConcurrentHashMap<String, String>()

    // Live LXMessage references keyed by message hash hex. Used by
    // cmdLxmfGetMessageState to read the *current* state from the
    // message itself. Required because LXMessage.deliveryCallback only
    // fires on the terminal transitions the router cares about
    // (DELIVERED for opportunistic, SENT for propagated): intermediate
    // transitions like opportunistic OUTBOUND→SENT (no proof yet) or
    // propagated OUTBOUND→SENDING (link established, transfer in flight)
    // never fire a callback, so outboundState alone reports stale
    // 'outbound' for messages that have actually advanced.
    val outboundMessages = ConcurrentHashMap<String, LXMessage>()

    // Set true at the top of cmdLxmfShutdown so any callbacks the router
    // schedules during teardown can't repopulate `outboundState` /
    // `inbox` after we've cleared them. The router's own callbacks may
    // fire on background threads after `router.stop()` returns.
    @Volatile var shuttingDown: Boolean = false

    // Started TCP interfaces (for shutdown).
    val interfaces = mutableListOf<Interface>()
}

// ----------------------------------------------------------------------
// Helpers
// ----------------------------------------------------------------------

private fun stateToString(state: MessageState?): String =
    when (state) {
        MessageState.GENERATING -> "generating"
        MessageState.OUTBOUND -> "outbound"
        MessageState.SENDING -> "sending"
        MessageState.SENT -> "sent"
        MessageState.DELIVERED -> "delivered"
        MessageState.FAILED -> "failed"
        MessageState.REJECTED -> "failed"
        MessageState.CANCELLED -> "failed"
        null -> "unknown"
    }

private fun methodToString(method: DeliveryMethod?): String =
    when (method) {
        DeliveryMethod.OPPORTUNISTIC -> "opportunistic"
        DeliveryMethod.DIRECT -> "direct"
        DeliveryMethod.PROPAGATED -> "propagated"
        else -> "unknown"
    }

/**
 * Pick an OS-assigned ephemeral port by binding then immediately closing
 * a probe socket.
 *
 * There is a small TOCTOU race here: between this function returning and
 * `TCPServerInterface.start()` actually binding to the returned port,
 * another process could in principle claim it. In practice this is a
 * non-issue for the conformance bridge:
 *
 *   - GitHub Actions runners isolate jobs onto separate VMs, so parallel
 *     CI jobs cannot race for the same loopback port.
 *   - Within a single bridge process this function is called serially,
 *     <1ms before TCPServerInterface binds — the kernel doesn't reassign
 *     a freshly-released ephemeral port that fast.
 *
 * If the upstream TCPServerInterface API ever gains support for reading
 * back its actual bound port after `start()` (analogous to ServerSocket's
 * `localPort` once `bind()` returns), we could pass `bindPort = 0` and
 * read the assignment back, eliminating the race entirely. Until then,
 * this is the cheapest workable approximation.
 */
private fun allocateFreePort(): Int {
    val s = ServerSocket()
    return try {
        s.bind(java.net.InetSocketAddress("127.0.0.1", 0))
        s.localPort
    } finally {
        s.close()
    }
}

private fun ensureRouter(name: String): LXMRouter =
    BridgeState.router
        ?: throw IllegalStateException("lxmf_init must be called before $name")

/** Terminal outbound states — once observed, never overwrite. */
private val TERMINAL_OUTBOUND_STATES = setOf("delivered", "failed")

/**
 * Record an outbound message state without downgrading a terminal one.
 *
 * Three writers race on `outboundState[hash]`: the per-message
 * `deliveryCallback` (any thread the router schedules its callbacks on),
 * the `failedCallback` (same), and the synchronous post-handleOutbound
 * write in each `cmdLxmf_send_*`. If a fast OPPORTUNISTIC delivery
 * completes its callback chain BEFORE the synchronous write reads
 * `message.state`, the synchronous write would overwrite a terminal
 * "delivered" with the now-stale "sending". Use compute() to keep
 * the latest terminal state once we see one.
 *
 * Also no-ops once `BridgeState.shuttingDown` is set so late-firing
 * callbacks don't repopulate the map after `cmdLxmfShutdown` cleared it.
 */
private fun recordOutboundState(hashHex: String, newState: String) {
    if (BridgeState.shuttingDown) return
    BridgeState.outboundState.compute(hashHex) { _, existing ->
        if (existing in TERMINAL_OUTBOUND_STATES) existing else newState
    }
}

// ----------------------------------------------------------------------
// LXMF field codec
// ----------------------------------------------------------------------
//
// LXMF `fields` is a `Map<Int, Any>` whose leaf values can be ByteArray,
// String, Int/Long, Boolean, or nested Lists/Maps; the wire format is
// msgpack and ByteArray vs String are *distinct* msgpack types. JSON-RPC
// can't carry bytes natively so the bridge contract uses tagged objects:
//   - {"bytes": "<hex>"}
//   - {"str": "..."}
//   - {"int": 123}
//   - {"bool": true}
//   - JSON arrays for lists
//   - bare JSON primitives pass through
//
// The explicit "bytes" tag is load-bearing for FIELD_FILE_ATTACHMENTS
// (id 0x05): each attachment is `[filename_str, data_bytes]` and without
// the tag we'd lose the bytes-vs-string distinction round-tripping
// through JSON. Mirrors reference/lxmf_python.py exactly.

private fun decodeFieldValue(value: Any?): Any? {
    if (value == null || value == JSONObject.NULL) return null
    return when (value) {
        is JSONObject -> when {
            value.has("bytes") -> hexToBytes(value.getString("bytes"))
            value.has("str") -> value.getString("str")
            value.has("int") -> value.getLong("int")
            value.has("bool") -> value.getBoolean("bool")
            else -> {
                // Bare object — treat as nested map with string keys.
                val out = mutableMapOf<String, Any?>()
                for (k in value.keys()) out[k] = decodeFieldValue(value.get(k))
                out
            }
        }
        is JSONArray -> {
            val out = mutableListOf<Any?>()
            for (i in 0 until value.length()) out.add(decodeFieldValue(value.get(i)))
            out
        }
        else -> value
    }
}

private fun decodeFieldsParam(params: JSONObject): MutableMap<Int, Any> {
    val raw = params.optJSONObject("fields") ?: return mutableMapOf()
    val out = mutableMapOf<Int, Any>()
    for (k in raw.keys()) {
        val decoded = decodeFieldValue(raw.get(k))
            ?: continue // null leaves don't survive into LXMF anyway
        // LXMF field IDs are decimal strings on the JSON-RPC wire. Surface
        // a clear validation error rather than leaking NumberFormatException
        // up through handleRequest, which would otherwise present as a raw
        // JVM stack trace to the test harness on a malformed payload.
        val id = k.toIntOrNull()
            ?: throw IllegalArgumentException(
                "fields key '$k' is not a decimal integer (LXMF field IDs are 0..255)",
            )
        out[id] = decoded
    }
    return out
}

// Encoder: convert ByteArray -> hex so the value is JSON-safe. Mirrors
// the python bridge's _encode_field_value_for_inbox; the test suite
// asserts on hex strings (not the tagged form) when reading the inbox.
private fun encodeFieldValueForInbox(value: Any?): Any? {
    return when (value) {
        null -> JSONObject.NULL
        is ByteArray -> value.toHexString()
        is List<*> -> JSONArray().also { arr ->
            for (item in value) arr.put(encodeFieldValueForInbox(item))
        }
        is Map<*, *> -> JSONObject().also { obj ->
            for ((k, v) in value) obj.put(k.toString(), encodeFieldValueForInbox(v))
        }
        is Long, is Int, is Double, is Float, is Boolean, is String -> value
        // Anything else: stringify to keep JSON valid; loud failure is
        // better than silently dropping the value.
        else -> value.toString()
    }
}

// Note on key coercion: LXMF top-level field IDs are Int (0..255),
// rendered as decimal-string keys on the JSON wire ("5" for
// FIELD_FILE_ATTACHMENTS, etc.) — that's the spec contract this codec
// implements. Nested maps with non-string keys (Int, ByteArray) get
// stringified via k.toString() and DO NOT round-trip back to their
// original key type on the receive side. None of the LXMF fields the
// conformance suite exercises today have nested non-string keys, but
// any future test that uses one will need explicit handling here.
private fun encodeMessageFields(message: LXMessage): JSONObject {
    val out = JSONObject()
    for ((k, v) in message.fields) {
        out.put(k.toString(), encodeFieldValueForInbox(v))
    }
    return out
}

